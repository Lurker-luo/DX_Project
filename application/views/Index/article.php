                                <h1 class="aTitle">
                                    校党委举办社会主义协商民主专题培训
                                </h1>
                            </div>
                            <div class="luru_c" style="text-align:center;">
                                <span>
                                    [日期：2015-05-12] &nbsp; 来源：党委统战部 新闻网&nbsp; 作者：同心 &nbsp; 阅读：310次
                                </span>
                                <hr style="margin-left:10px;">
                            </div>
                            <div id="con">
                                <!--主题内容开始-->
                                <ul>
                                    <li>
                                        <p>
                                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 5月11日，校党委举办社会主义协商民主专题培训。校党委书记高文兵、湖南省社会主义学院副院长周述杰分别作了“创新高校协商民主需要把握五大概念贯通五
                                            个环节”“加强社会主义协商民主建设”的专题报告。培训会议由校党委副书记高山主持，省委统战部副部长崔永平、省侨联原主席曹亚、省委统战部知工处处长盛
                                            完善应邀出席，我校统战工作领导小组成员、二级党组织书记、统战委员、统战干事、各民主党派基层组织和统战团体负责人、各级人大代表和政协委员共计120
                                            余人参加培训。 &nbsp; &nbsp;&nbsp;
                                        </p>
                                        <p>
                                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 高文兵书记的报告以“国旗的五星与协商民主的内在联系”为引子，通过分析中西民主差异，论述了社会主义协商民主是中国社会主义民主政治的特有形式和独特优
                                            势，是党的群众路线在政治领域的重要体现，是深化政治体制改革、建设社会主义政治文明，推进国家治理体系和治理能力现代化的重要内容；同时总结了我校协商
                                            民主实践过程中的平台搭建、议题与目标、制度建设、效果及不足，进一步指出：协商民主制度在把握“理性、法治、科层制、发展、和谐”五大核心概念的基础
                                            上，贯通“充分表达、发现异议、解决异议、达成共识、共享成果”五个环节，从而实现“求同存异”，最终发展共识，社会主义协商民主制度的无比优越性就会充
                                            分体现。 &nbsp; &nbsp;&nbsp;
                                        </p>
                                        <p>
                                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 周述杰副院长在报告中首先从“积极应对世情国情变化和各种挑战的必然要求、破除民主建设思想误区选择正确民主发展道路的客观要求、深入贯彻中共中央《关于
                                            加强社会主义协商民主建设的意见》的现实需要”等三个角度解释为什么要加强社会主义协商民主建设，进而从“明确指导思想、坚持基本原则、拓展协商渠道、遵
                                            循协商程序、加强党对协商民主建设的领导”等五个方面论述如何加强社会主义协商民主建设。 &nbsp; &nbsp;&nbsp;
                                        </p>
                                        <p class="vsbcontent_img" style="text-align: center">
                                            <img style="cursor:pointer;" onclick="openURL(this.src)" onload="zoomIMG(this,625)"
                                            id="20ra408uzm" src="article_files/20ra408uzm.jpg" alt="" hspace="0" border="0"
                                            width="500">
                                        </p>
                                        <p>
                                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 当日上午，学校还举行了《同心铸辉煌---中南大学统一战线发展史》首发式。该书共计60余万字，历经三年完成。该书集综合性、工具性、指导性于一体，全
                                            面梳理了中南大学合并前后60多年的统一战线历史脉络，全方位地记述了从新中国成立到改革开放到新世纪新阶段，在我党统一战线发展的大格局下，前期三校以
                                            及合并组建后的发展历程，涉及多党合作与政治协商、民族、宗教、党外知识分子、海外联谊各领域，以及民主党派基层组织建设、党外代表人士队伍建设、理论研
                                            究、统战信息与宣传、党外调研等工作范围，从多方位多角度构成了中南大学统一战线的一幅历史画卷。 &nbsp; &nbsp;&nbsp;
                                        </p>
                                        <p>
                                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 培训期间，校党委宣布成立”中南大学统一战线参政议政工作室”，旨在通过统一战线渠道及时有效地反映社会问题、社情民意与各方诉求，为进一步提高我校统一
                                            战线代表人士依法履职能力和参政议政水平提供积极帮助。工作室挂靠法学院，由统战部与法学院共同管理。工作室将为我校以各级人大代表和政协委员为主体的统
                                            一战线代表人士配备法律助理，提供法律咨询，协助他们收集信息素材、调查研究、撰写提案议案。 &nbsp; &nbsp;
                                        </p>
                                        <p class="vsbcontent_img" style="text-align: center">
                                            <img style="cursor:pointer;" onclick="openURL(this.src)" onload="zoomIMG(this,625)"
                                            src="article_files/255etq6uxm.jpg" alt="" hspace="0" border="0" width="500">
                                        </p>
                                        <p class="vsbcontent_end">
                                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 当天下午，学校各民主党派基层组织、统战团体负责人对上午的两个专题报告进行了讨论。对民建支部、农工党校本部支部、民进校本部支部、民盟铁道总支部、农
                                            工党铁道支部、民盟校本部委员会、民革湘雅总支部等7个“2013-2014年度统一战线先进集体”进行了表彰，其中获奖的四个民主党派基层组织分别从理
                                            论研究、社会服务、组织建设、信息宣传等方面介绍了各自的做法和经验。&nbsp;&nbsp;
                                        </p>
                                    </li>
                                </ul>
